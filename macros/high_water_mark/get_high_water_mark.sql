{% macro get_high_water_mark(timestamp_column='loaded_timestamp', predicate=none, warehouse_size='xs', model_name=this.name) %}

    {% set macro_polo = var('macro_polo', {}) %}
    {% set model_id = this.schema ~ "." ~ this.name if this else 'unknown_model' %}
    {% set macro_name = 'get_high_water_mark' %}

    {# Input validation #}
    {% if not timestamp_column %}
        {% set msg = "Configuration Error: timestamp_column is required and needs to be defined as string literal or optionally in model config." %}
        {{ dbt_macro_polo.log_event(message=msg, level='ERROR', model_id=model_id, macro_name=macro_name) }}
    {% endif %}

    {% if execute %}

        {# Cache handling #}
        {% set state_key = '_macro_polo_hwm_' ~ model_id | replace('.', '_') ~ '_' ~ timestamp_column  ~ ('_' ~ predicate | replace(' ', '_') if predicate is not none else '') %}
        {% set state_value = dbt_macro_polo.get_runtime_state(state_key) %}
    
        {% if state_value %}
            {{ dbt_macro_polo.log_event(message="Resolved high water mark from runtime state", model_id=model_id, status=state_value | upper, macro_name=macro_name) }}
            {{ return("'" ~ state_value ~ "'::timestamp") }}
        {% endif %}

        {# Warehouse allocation #}
        {% set warehouse = dbt_macro_polo.provision_compute(warehouse_size) %}
        {% if not warehouse %}
            {{ dbt_macro_polo.log_event(message="Failed to allocate warehouse", level='ERROR', model_id=model_id, macro_name=macro_name) }}
        {% endif %}

        {# Get relation with validation #}
        {% set relation = adapter.get_relation(this.database, this.schema, model_name) if model_name is not none else this %}
        {% if not relation %}
            {{ dbt_macro_polo.log_event(message="Relation not found: " ~ model_name, level='ERROR', model_id=model_id, macro_name=macro_name) }}
        {% endif %}

        {# Build and execute query #}
        {% set query %}
            /* Query generated by {{ macro_name }} macro */
            select coalesce(max({{ timestamp_column }}), '0'::timestamp) as max_ts
            from {{ relation }}
            {% if predicate %}
                where {{ predicate }}
            {% endif %}
        {% endset %}

        {# Set warehouse and execute query #}
        {% do run_query('use warehouse ' ~ warehouse) %}
        {% set result = run_query(query) %}
        {# Reset warehouse otherwise dbt will use the previous warehouse size #}
        {% do run_query('use warehouse ' ~ target.warehouse) %}

        {% if not result %}
            {{ dbt_macro_polo.log_event(message="Query execution failed", level='ERROR', model_id=model_id, macro_name=macro_name) }}
        {% endif %}

        {# Process and validate result #}
        {% set timestamp = result.columns[0].values()[0] %}
        {% if timestamp is none %}
            {{ dbt_macro_polo.log_event(message="No timestamp value returned", level='ERROR', model_id=model_id, macro_name=macro_name) }}
        {% endif %}

        {# Cache and return result #}

        {# Convert timestamp to string for logging #}
        {% set timestamp_str = timestamp | string %}
        {{ dbt_macro_polo.log_event(message="Saving high water mark '" ~ timestamp_str ~ "' to runtime state with key '" ~ state_key ~ "'", model_id=model_id, level='DEBUG', macro_name=macro_name) }}
        {# Update: Use runtime_state instead of cache #}
        {% do macro_polo.get('runtime_state', {}).update({state_key: timestamp_str}) %}
        {{ dbt_macro_polo.log_event(message="Resolved high water mark", model_id=model_id, status=timestamp_str, macro_name=macro_name) }}
        {{ return("'" ~ timestamp_str ~ "'::timestamp") }}

    {% endif %}

{% endmacro %}
